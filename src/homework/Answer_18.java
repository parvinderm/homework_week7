package homework;/*Write the java program to check if enter number is “POSITIVE”, “NEGATIVE” or
“ZERO”*/

public class Answer_18 {   //Declare Class

    public static void main(String[] args) {    //Declare main method or user defined method
        int a = 13;      //Declare Local variable

        if(a>0) {
            System.out.println("Positive");
        }else if (a<0){
            System.out.println("Negative");
        }else {
            System.out.println("Zero");
        }
    }
}
